package PersonPack;

public class MyFirstClass
{
    // 데이터와 행위(메소드)로 구성된다
    public int number; // 필드, 멤버 변수, 속성
    public String myString; //

    // 생성자(Constructor)
    public MyFirstClass() // 클래스명과 이름이 같아야 됨, 생성자에는 데이터 타입이 없다.
    {
        // 생성자는 보통 일반적으로 해당 클래스의 데이터들을 초기화할 목적으로 사용한다.
        number = 0;
        myString = "";
    }

    // public int
}
